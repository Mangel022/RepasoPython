conjunto = {1, 2, 3, 4, 5} #no acepta datos repetidos

conjunto.add(20) #*add(value) añae un nuevo dato
conjunto.remove(20) #*remove(value) eliminar el dato que le pasemos en el parametro
conjunto.discard(20) #*discard(value) elimina el dato que le pasemos en el parametro
conjunto.pop() #*pop() toma cualquiera al azar y lo elimina
conjunto.update([1,2,3]) #*update(value) permite añadir elementos
conjunto.clear() #*clear() elimina todos los datos