lista = [5, 3, 1, 4, 2]

lista.append(6) #*metodo append() agrega los datos al final de la lista
print(lista)

lista.insert(2, 2.5) #*metodo insert() agrega los datos en donde le indiquemos index, valor
print(lista)

print(lista.count(5)) #*count(valor) cuantos elementos hay en esa lista con es valor que le indicamos

print(lista.index(4)) #*index(valor) busca en toda la lista el primer valor que se le indique

lista.sort() #*sort() va ordenar la lista ascendente
print(lista)

lista.reverse() #*reverse() ordena la lista en orden decendente
print(lista)





