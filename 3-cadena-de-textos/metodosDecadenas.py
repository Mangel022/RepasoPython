cadena = 'estoy utilizando los metodos de Python'

print(cadena.lower()) #toda la cadena en minusculas
print(cadena.upper()) #toda la cadena en mayusculas
print(cadena.capitalize()) #la primera palabra en mayuscula
print(cadena.title()) #cada cadena la primera letra en mayusculas
print(cadena.swapcase()) #altera minusculas en mayusculas - mayusculas en minusculas