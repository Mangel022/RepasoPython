import math
import random

print(math.pow(10, 2)) #elevacion
print(math.sqrt(64)) #raiz cuadrada flotante
print(math.isqrt(64)) #raiz cuadrada entera
print(math.sin(90)) #seno
print(math.cos(90)) #coseno
print(math.tan(90)) #tangente
print(math.factorial(5)) #factorial

print(random.randint(1, 100)) #muestra un numero al azar